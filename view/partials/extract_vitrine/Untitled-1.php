A PROPOS DE NOUS :
BÉTHEL COACHING signifie la porte des cieux pour une transformation parfaite en vue d'incarner la vraie VERSION DE VOUS-MÊME. La porte des cieux signifie la LUMIÈRE qui nous permet de nous découvrir pour nous VALORISER à travers l'acceptation de soi. Car les ténèbres de la pensée nous prive de notre vraie IDENTITÉ.
Et nous croyons que chaque être humain est une bénédiction pour lui-même, sa famille, son entourage, son entreprise, sa communauté et au-delà à travers son POTENTIEL. Mais avant de manifester cette bénédiction, il faut d'abord se découvrir et s'accepter. Car c'est ainsi que le lion qui dort en nous se réveillera et notre vie deviendra une vraie histoire D'IMPACT et D'INFLUENCE.
Avec l'expérience et en force de conseiller les gens, on a finit par comprendre que beaucoup de personnes ont un seul problème: c'est être étrangère envers soi-même . Beaucoup ignore leur vraie identité par faute de connaissance. C'est pourquoi ils souffrent et ne sont pas heureux ni capable de se valoriser.
C'est ainsi, qu'on a décidé de mettre BÉTHEL COACHING sur place pour aider beaucoup de personnes  à se découvrir pour accomplir parfaitement la mission GLORIEUSE de leur existence à travers la valorisation de leurs POTENTIEL qui était caché en eux. Mais surtout de pouvoir vivre leur vraie vie.
